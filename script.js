/* LOGIN */

function login() {

  const username =
    document
      .getElementById('username')
      .value
      .trim();

  const password =
    document
      .getElementById('password')
      .value
      .trim();

  const error =
    document.getElementById('login-error');

  if (
    username === 'admin' &&
    password === 'admin'
  ) {

    document
      .getElementById('login-screen')
      .classList
      .remove('active');

    document
      .getElementById('menu-screen')
      .classList
      .add('active');

    error.textContent = '';

  } else {

    error.textContent =
      'Incorrect username or password.';
  }
}

/* LOGOUT */

function logout() {

  document
    .getElementById('menu-screen')
    .classList
    .remove('active');

  document
    .getElementById('login-screen')
    .classList
    .add('active');

  document
    .getElementById('username')
    .value = '';

  document
    .getElementById('password')
    .value = '';

  showHome();
}

/* HOME */

function showHome() {

  document
    .getElementById('home-view')
    .style.display = '';

  document
    .getElementById('bio-view')
    .style.display = 'none';

  closeAllNav();

  window.scrollTo(0, 0);
}

/* NAVIGATION */

function closeAllNav() {

  document
    .querySelectorAll('#nav-links > li')
    .forEach(li => {
      li.classList.remove('open');
    });
}

function toggleNav(e, index) {

  e.preventDefault();

  const items =
    document.querySelectorAll('#nav-links > li');

  items.forEach((li, i) => {

    if (i === index) {
      li.classList.toggle('open');
    } else {
      li.classList.remove('open');
    }
  });
}

/* BIOGRAPHY PAGE */

function showBio(e, key) {

  if (e) e.preventDefault();

  closeAllNav();

  const artist = ARTISTS[key];

  if (!artist) return;

  const sectionsHTML =
    artist.sections
      .map(section => `
        <div class="bio-section">

          <p class="bio-section-label">
            ${section.label}
          </p>

          <h2>
            ${section.heading}
          </h2>

          <p>
            ${section.text}
          </p>

        </div>
      `)
      .join('');

  document
    .getElementById('bio-content')
    .innerHTML = `

      <button
        class="bio-back"
        onclick="showHome()"
      >
        ← Back to Archive
      </button>

      <div class="bio-header">

        <div class="bio-img-wrap">

          <img
            src="${artist.image}"
            alt="${artist.name}"
          />

        </div>

        <div class="bio-intro">

          <span class="bio-tag">
            ${artist.tag}
          </span>

          <h1 class="bio-name">
            ${artist.name}
          </h1>

          <p class="bio-dates">
            ${artist.dates}
          </p>

          <p class="bio-summary">
            ${artist.summary}
          </p>

          <a
            href="${artist.musicUrl}"
            target="_blank"
            class="bio-music-btn"
          >
            ▶ Listen on YouTube
          </a>

        </div>

      </div>

      ${sectionsHTML}
    `;

  document
    .getElementById('home-view')
    .style.display = 'none';

  document
    .getElementById('bio-view')
    .style.display = 'flex';

  window.scrollTo(0, 0);
}

/* LOGIN EVENTS */

document
  .getElementById('login-btn')
  .addEventListener(
    'click',
    login
  );

document
  .addEventListener('keydown', e => {

    if (
      e.key === 'Enter' &&
      document
        .getElementById('login-screen')
        .classList
        .contains('active')
    ) {
      login();
    }
  });

/* CLOSE DROPDOWNS */

document
  .addEventListener('click', e => {

    if (
      !e.target.closest('#nav-links')
    ) {
      closeAllNav();
    }
  });